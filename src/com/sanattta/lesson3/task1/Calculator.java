package com.sanattta.lesson3.task1;

public class Calculator {

    int add(int numberFirst, int numberSecond) {
        return numberFirst + numberSecond;
    }

    int difference(int numberFirst, int numberSecond) {
        return numberFirst - numberSecond;
    }

    int multiply(int numberFirst, int numberSecond) {
        return numberFirst * numberSecond;
    }

    int division(int numberFirst, int numberSecond) {
        return numberFirst / numberSecond;
    }

}